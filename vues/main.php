<h1>Cardio-Referent : plugin de gestion du référent et de son club :</h1>
<br>
<h4>Pour bien utiliser ce plugin je dois :</h4>
<ul>
    <li style="list-style-type: square;">Le login est unique et ne peut être modifié</li>
    <li style="list-style-type: square;">Créer un référent puis un club pour lui attribuer les droits</li>
    <li style="list-style-type: square;">Supprimer un référent via le plugin (Sinon il existe un toujours le club avec ce référent)</li>
</ul>
